// Suma de elementos: Escribir un programa que cree un array de 3 enteros e
// imprima por consola la suma de todos sus elementos, sin usar el bucle for.

public class SumaDeElementos {
    public static void main(String[] args) {
        //Crear un array de 3 enteros
        int[] array = {2, 4, 6};

        //Calcular la suma de los elementos del array
        int suma = array[0] + array[1] + array[2];

        //Imprimir la suma por consola
        System.out.println("La suma de los elementis es: " + suma);
    }
}
